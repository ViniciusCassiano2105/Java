package br.com.repetindo_instrucoes;
//Escreva um programa Java que imprime números de 1 à 10 
//utilizando a estrutura de repetição while. 
//O programa deve imprimir cada número em uma linha diferente.
public class Exercicio1_Contando_de_1_a_10 {

	public static void main(String[] args) {
		int i;
		
		
		for (i = 1; i <= 10; i++) {
			if(i>=1) {
			System.out.println(i);
			}
		
			
		}

	}

}
